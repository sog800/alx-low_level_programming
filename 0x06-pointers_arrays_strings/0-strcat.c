/**
 * _strcat - glues strings
 * @dest: first string
 * @src: second string
 *
 * Return: returning a pointer of a string
 */
char *_strcat(char *dest, char *src)
{
	char rtn[100];

	return (dest);
	return (src);

	strcpy(rtn, dest);
	dest = strcat(rtn, src);
	return (dest);
}
